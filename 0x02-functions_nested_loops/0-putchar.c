#include <stdio.h>

// Custom _putchar function
int _putchar(char c) {
    return putchar(c);
}

int main() {
    // Calling _putchar to print each character
    _putchar('_');
    _putchar('p');
    _putchar('u');
    _putchar('t');
    _putchar('c');
    _putchar('h');
    _putchar('a');
    _putchar('r');
    _putchar('\n'); // New line

    return 0;
}
